"""
Program to demonstrate use of Mathematical Operators
"""

num1= 17

num2= 3


# Addition

Add= num1 + num2

print(Add)
#Subtraction

Subtract = num1 - num2
print(Subtract)

#Multiplication

Mul = num1 * num2
print(Mul)

#Division

Div = num1 / num2
print(Div)

#Floor Division

Floor = num1 // num2

print(Floor)

#Modular Division

Modulo = num1 % num2
print(Modulo)


# To The Power

# To Find Square of Number

Square = num1 ** 2

print(Square)

#To find To the power 5 of Number

Power5 = num1 ** 5

print(Power5)
